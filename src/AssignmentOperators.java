
public class AssignmentOperators {

	public static void main(String[] args) {
		
		int a = 16;
		 
	   a += 20; 
	   System.out.println("the value obtained  after addition is " + a);
	   
	   a -= 50;
	   System.out.println("the value obtained  after subtraction is " + a);
	   
	   a *= 8;
	   System.out.println("the value obtained after multiplication  is " + a);
	   
	   a /= 4;
	   System.out.println("the value obtained after division is " + a);
	   
	   a %= 2;
	   System.out.println("the  remainder value  is " + a);
	   
	   
		

	}

}
