
public class Operators {

	public static void main(String[] args) {
	
		// arithmetic operators examples
		 
		int a = 52, b = 65, z = 10;
		double d = 58, d1 = 5e4;
		
		//addition
		int c = a + b + z;
		double d2 = d + d1;
		
		//subtract
		int e = a - b - z;
		double d4 = d - d1;
		
		//multiplication
		int f = a * b;
		double d5 = d * d1;
		
		//division
		int g = a / b;
		double d6 = d / d1;
		
		//modulo 
		int h = a % b % z;
		double d7 = d % d1;
		
		System.out.println(" addition  values of both int and double = " + c + " , " + d2);
		System.out.println(" subtraction values of both int and double = " + e + " , " + d4);
		System.out.println(" multiplication values of both int and double =  " + f + " , " + d5);
		System.out.println(" division values of both int and double  = " + g + " , " + d6);
		System.out.println(" remainder values of both int and double = " + h + " , " + d7);
		

	}

}
