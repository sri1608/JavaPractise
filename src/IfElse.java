
public class IfElse {

	public static void main(String[] args) {
		
		int a = 23;
		if(a > 50 ) {
			System.out.println("the value " + a + " is greater than 50");
		}else {
			System.out.println("the value " + a + " is less than 50");
		} 
		
		
		
		int b = 75, c = 88;
		 
		if(b <= c) {
			System.out.println("The value " + b + " is less than or equal to "+ c);
		}else {
			System.out.println("The value " + b + " is greater than or equal to "+ c);
		}
		
		
	}


}
